package ex07.func_prog;

public class Employee {
	private double bs;
	private double ded;
	private double add;

	public Employee(double bs, double ded, double add) {
		this.bs = bs;
		this.ded = ded;
		this.add = add;
	}

	public double getBs() {
		return bs;
	}

	public double getDed() {
		return ded;
	}

	public double getAdd() {
		return add;
	}

}
